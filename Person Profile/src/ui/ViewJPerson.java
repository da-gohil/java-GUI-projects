    private void populateTable() {
    
    DefaultTableModel model = (DefaultTableModel) tblPerson.getModel();
    model.setRowCount(0);

    for (Person p1: history.getHistory()){
    
        Object[] row = new Object[4];
        row[0] = p1;
        row[1] = p1.getmName();
        row[2] = p1.getlName();
        row[3] = p1.getDob();
        model.addRow(row);
   
    }
}
