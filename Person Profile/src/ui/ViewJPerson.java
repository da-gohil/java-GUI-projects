    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblPerson.getSelectedRow();
        
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please a select a row to view");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel)tblPerson.getModel();
        Person selectedPerson = (Person)model.getValueAt(selectedRowIndex,0);
        
        txtfName.setText(String.valueOf(selectedPerson.getfName()));
        txtmName.setText(String.valueOf(selectedPerson.getmName()));
        txtlName.setText(String.valueOf(selectedPerson.getlName()));
        txtDate.setText(String.valueOf(selectedPerson.getDob()));
        txtPhone.setText(String.valueOf(selectedPerson.getPhone()));
          
    }//GEN-LAST:event_btnViewActionPerformed

