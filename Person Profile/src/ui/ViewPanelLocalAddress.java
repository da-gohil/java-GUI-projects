    private void btnViewLocalAddressActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewLocalAddressActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblLocalAddress.getSelectedRow();

        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please a select a row to view");
            return;
        }

        DefaultTableModel localmodel = (DefaultTableModel)tblLocalAddress.getModel();
        LocalAddress selectedLocalAddress = (LocalAddress)localmodel.getValueAt(selectedRowIndex,0);

        txtName.setText(String.valueOf(selectedLocalAddress.getName()));
        txtUnit.setText(String.valueOf(selectedLocalAddress.getUnit()));
        txtStreet.setText(String.valueOf(selectedLocalAddress.getStreet()));
        txtCity.setText(String.valueOf(selectedLocalAddress.getCity()));
        txtState.setText(String.valueOf(selectedLocalAddress.getState()));
        txtCountry.setText(String.valueOf(selectedLocalAddress.getCountry()));
    }//GEN-LAST:event_btnViewLocalAddressActionPerformed

    private void btnDeleteLocalAddressActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteLocalAddressActionPerformed
        // TODO add your handling code here:

        int selectedRowIndex = tblLocalAddress.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
            return;
        }

        DefaultTableModel localmodel = (DefaultTableModel)tblLocalAddress.getModel();
        
        LocalAddress selectedLaddress = (LocalAddress)localmodel.getValueAt(selectedRowIndex, 0);

        localhistory.deleteLocalAddress(selectedLaddress);

        JOptionPane.showMessageDialog(this, "Local Address deleted.");
        populateLocalTable();

        txtName.setText("");
        txtUnit.setText("");
        txtState.setText("");
        txtStreet.setText("");
        txtCity.setText("");
        txtCountry.setText("");
    }//GEN-LAST:event_btnDeleteLocalAddressActionPerformed

