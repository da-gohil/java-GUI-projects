    private void populateLocalTable() {

    DefaultTableModel localmodel = (DefaultTableModel) tblLocalAddress.getModel();
    localmodel.setRowCount(0);

       for (LocalAddress L1: localhistory.getLocalhistory()){

                Object[] row = new Object[5];
                row[0] = L1;
                row[1] = L1.getUnit();
                row[2] = L1.getStreet();
                row[3] = L1.getCity();
                row[4] = L1.getState();
                
                localmodel.addRow(row);
        }
    }  
