    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex= tblBankAccount.getSelectedRow();

        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this,"Please select a row to view");
            return;
        }
        DefaultTableModel model = (DefaultTableModel)tblBankAccount.getModel();
        BankAccount selectedAccount = (BankAccount)model.getValueAt(selectedRowIndex,0);

        txtName.setText(String.valueOf(selectedAccount.getName()));
        txtAccNumber.setText(String.valueOf(selectedAccount.getAccountNumber()));
        txtAccType.setText(String.valueOf(selectedAccount.getAccountType()));
        txtBalance.setText(String.valueOf(selectedAccount.getBalance()));
        txtLimit.setText(String.valueOf(selectedAccount.getCreditLimit()));
        txtDate.setText(String.valueOf(selectedAccount.getOpeningDate()));
                                               
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex= tblBankAccount.getSelectedRow();
        
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this,"Please select a row to delete");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblBankAccount.getModel();
        BankAccount selectedAccount=(BankAccount)model.getValueAt(selectedRowIndex, 0);
        bankhistory.deleteBankAccount(selectedAccount);
        
        JOptionPane.showMessageDialog(this,"The Bank account has been deleted");
        populateTable();
        
        txtAccType.setText("");
        txtBalance.setText("");
        txtAccNumber.setText("");
        txtLimit.setText("");
        txtDate.setText("");
        txtName.setText("");
    }//GEN-LAST:event_btnDeleteActionPerformed



