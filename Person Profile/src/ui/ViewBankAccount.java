    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblBankAccount.getModel();
        model.setRowCount(0);
        
        for (BankAccount b : bankhistory.getBankhistory()){
            Object[] row = new Object[6];
            row[0] = b;
            row[1] = b.getAccountType();
            row[2] = b.getAccountNumber();
            row[3] = b.getBalance();
            row[4] = b.getCreditLimit();
            row[5] = b.getOpeningDate();
            model.addRow(row);
        }    
    }

